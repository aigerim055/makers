'''
SQL - Structured Query Language - структурированный язык запросов - язык общения с базами данных

База данных (БД) - организованная структура для хранения больших объемов данных

Системы управления базами данных (СУБД) - программа для управления организацией баз данных (создание. обновления. удаления, получение)

PostgreSQL, Access, MySQL, Oracle, Redis...

Реляционные - имеют связи между таблицами

Нереляционные - не имеют связей (ключ, значение)

PostgreSQL - реляционная СУБД, имеет встроенные функции, открытый исходный код, частые обновления, соответствует стандарты 

ACID(atomicity - атомарность, consistency - согласованность, isolation - изолированность, durability - стойкость)

категории запросов:
DDL (data definition language) - работа над таблицами (create, alter, drop)
DML (data manipulation language) - работа с данными (select, insert, update, delete)
TCL (transaction control language) - работа с транзакциями (commit, rollback, savepoint)
'''